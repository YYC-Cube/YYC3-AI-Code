/* ================================================================
   I18n Service Implementation
   ================================================================ */

class I18nServiceImpl implements I18nService {
  private config: I18nConfig
  private resources: I18nResources
  private missingKeys: Array<{ key: string; ns: string; locale: string }> = []
  private listeners: Array<(locale: SupportedLocale) => void> = []

  constructor() {
    this.config = { ...DEFAULT_CONFIG }
    this.resources = resources
    this.detectAndApplyLocale()
  }

  /* ── Locale detection ── */

  private detectAndApplyLocale(): void {
    if (this.config.persist) {
      try {
        const saved = localStorage.getItem(this.config.storageKey)
        if (saved && this.isValidLocale(saved)) {
          this.config.locale = saved as SupportedLocale
          log.info(`Restored locale from storage: ${saved}`)
          return
        }
      } catch { /* localStorage unavailable */ }
    }

    if (this.config.autoDetect) {
      const browserLang = navigator?.language || navigator?.languages?.[0]
      if (browserLang) {
        const matched = this.matchLocale(browserLang)
        if (matched) {
          this.config.locale = matched
          log.info(`Auto-detected locale: ${matched}`)
          return
        }
      }
    }

    log.info(`Using default locale: ${this.config.locale}`)
  }

  private matchLocale(browserLang: string): SupportedLocale | null {
    const exact = Object.keys(LOCALE_INFO).find(
      l => l.toLowerCase() === browserLang.toLowerCase()
    )
    if (exact) {return exact as SupportedLocale}

    const prefix = browserLang.split('-')[0].toLowerCase()
    const match = Object.keys(LOCALE_INFO).find(
      l => l.toLowerCase().startsWith(prefix)
    )
    return match ? (match as SupportedLocale) : null
  }

  private isValidLocale(locale: string): boolean {
    return locale in LOCALE_INFO
  }

  /* ── Core translation ── */

  t(key: string, nsOrOptions?: TranslationNamespace | InterpolationOptions, options?: InterpolationOptions): string {
    let ns: TranslationNamespace = this.config.defaultNamespace
    let opts: InterpolationOptions = {}

    if (typeof nsOrOptions === 'string') {
      ns = nsOrOptions
      opts = options || {}
    } else if (nsOrOptions) {
      opts = nsOrOptions
    }

    // Try current locale
    let value = this.resolve(this.config.locale, ns, key)

    // Fallback locale
    if (value === undefined && this.config.locale !== this.config.fallbackLocale) {
      value = this.resolve(this.config.fallbackLocale, ns, key)
    }

    // Missing key
    if (value === undefined) {
      this.recordMissing(key, ns, this.config.locale)
      return key
    }

    // Interpolation
    return this.interpolate(value, opts)
  }

  exists(key: string, ns?: TranslationNamespace): boolean {
    const namespace = ns || this.config.defaultNamespace
    return this.resolve(this.config.locale, namespace, key) !== undefined ||
           this.resolve(this.config.fallbackLocale, namespace, key) !== undefined
  }

  private resolve(locale: SupportedLocale, ns: TranslationNamespace, key: string): string | undefined {
    const nsData = this.resources[locale]?.[ns]
    if (!nsData) {return undefined}

    // Flat key lookup
    const val = (nsData as Record<string, string>)[key]
    if (typeof val === 'string') {return val}

    // Dot-notation deep lookup
    const parts = key.split('.')
    let current: any = nsData
    for (const part of parts) {
      if (current == null || typeof current !== 'object') {return undefined}
      current = current[part]
    }
    return typeof current === 'string' ? current : undefined
  }

  private interpolate(template: string, values: InterpolationOptions): string {
    const { prefix, suffix } = this.config.interpolation
    let result = template

    for (const [k, v] of Object.entries(values)) {
      if (v === undefined) {continue}
      const placeholder = `${prefix}${k}${suffix}`
      result = result.split(placeholder).join(String(v))
    }

    return result
  }

  private recordMissing(key: string, ns: string, locale: string): void {
    const exists = this.missingKeys.some(m => m.key === key && m.ns === ns && m.locale === locale)
    if (!exists) {
      this.missingKeys.push({ key, ns, locale })
      if (this.config.debug) {
        log.warn(`Missing translation: [${locale}] ${ns}:${key}`)
      }
    }
  }

  /* ── Locale management ── */

  getLocale(): SupportedLocale {
    return this.config.locale
  }

  setLocale(locale: SupportedLocale): void {
    if (!this.isValidLocale(locale)) {
      log.warn(`Invalid locale: ${locale}`)
      return
    }

    const prev = this.config.locale
    this.config.locale = locale

    if (this.config.persist) {
      try { localStorage.setItem(this.config.storageKey, locale) } catch { /* */ }
    }

    // Set document direction
    if (typeof document !== 'undefined') {
      document.documentElement.lang = locale
      document.documentElement.dir = LOCALE_INFO[locale].direction
    }

    log.info(`Locale changed: ${prev} → ${locale}`)

    for (const listener of this.listeners) {
      try { listener(locale) } catch { /* */ }
    }
  }

  getSupportedLocales(): LocaleInfo[] {
    return Object.values(LOCALE_INFO)
  }

  getLocaleInfo(locale: SupportedLocale): LocaleInfo {
    return LOCALE_INFO[locale]
  }

  /* ── Formatting: Date ── */

  formatDate(date: Date | number | string, options?: DateFormatOptions): string {
    const d = new Date(date)
    const locale = this.config.locale
    const style = options?.style || 'date'

    switch (style) {
      case 'date':
        return new Intl.DateTimeFormat(locale, { year: 'numeric', month: '2-digit', day: '2-digit', timeZone: options?.timezone }).format(d)
      case 'datetime':
        return new Intl.DateTimeFormat(locale, { year: 'numeric', month: '2-digit', day: '2-digit', hour: '2-digit', minute: '2-digit', second: '2-digit', timeZone: options?.timezone }).format(d)
      case 'time':
        return new Intl.DateTimeFormat(locale, { hour: '2-digit', minute: '2-digit', second: '2-digit', timeZone: options?.timezone }).format(d)
      case 'short':
        return new Intl.DateTimeFormat(locale, { month: 'short', day: 'numeric', timeZone: options?.timezone }).format(d)
      case 'long':
        return new Intl.DateTimeFormat(locale, { year: 'numeric', month: 'long', day: 'numeric', weekday: 'long', timeZone: options?.timezone }).format(d)
      case 'relative':
        return this.formatRelativeTime(d)
      default:
        return new Intl.DateTimeFormat(locale).format(d)
    }
  }

  /* ── Formatting: Relative time ── */

  formatRelativeTime(date: Date | number): string {
    const d = new Date(date)
    const now = Date.now()
    const diffMs = now - d.getTime()
    const diffSec = Math.floor(diffMs / 1000)
    const diffMin = Math.floor(diffSec / 60)
    const diffHour = Math.floor(diffMin / 60)
    const diffDay = Math.floor(diffHour / 24)

    if (diffSec < 60) {return this.t('time.justNow')}
    if (diffMin < 60) {return this.t('time.minutesAgo', { count: diffMin })}
    if (diffHour < 24) {return this.t('time.hoursAgo', { count: diffHour })}
    if (diffDay === 1) {return this.t('time.yesterday')}
    if (diffDay < 30) {return this.t('time.daysAgo', { count: diffDay })}

    return this.formatDate(d, { style: 'date' })
  }

  /* ── Formatting: Number ── */

  formatNumber(value: number, options?: NumberFormatOptions): string {
    const locale = this.config.locale
    const intlOpts: Intl.NumberFormatOptions = {}

    if (options?.style === 'currency') {
      intlOpts.style = 'currency'
      intlOpts.currency = options.currency || LOCALE_INFO[locale].currency
    } else if (options?.style === 'percent') {
      intlOpts.style = 'percent'
      intlOpts.minimumFractionDigits = options?.minimumFractionDigits ?? 2
      intlOpts.maximumFractionDigits = options?.maximumFractionDigits ?? 2
    } else if (options?.style === 'unit' && options.unit) {
      intlOpts.style = 'unit'
      intlOpts.unit = options.unit
    } else {
      intlOpts.style = 'decimal'
    }

    if (options?.minimumFractionDigits !== undefined) {intlOpts.minimumFractionDigits = options.minimumFractionDigits}
    if (options?.maximumFractionDigits !== undefined) {intlOpts.maximumFractionDigits = options.maximumFractionDigits}

    return new Intl.NumberFormat(locale, intlOpts).format(value)
  }

  formatCurrency(value: number, currency?: string): string {
    return this.formatNumber(value, {
      style: 'currency',
      currency: currency || LOCALE_INFO[this.config.locale].currency,
    })
  }

  formatPercent(value: number): string {
    return this.formatNumber(value, { style: 'percent' })
  }

  /* ── Resources ── */

  addResources(locale: SupportedLocale, ns: TranslationNamespace, res: Record<string, string>): void {
    if (!this.resources[locale]) {
      (this.resources as any)[locale] = {}
    }
    const existing = this.resources[locale][ns] || {}
    ;(this.resources[locale] as any)[ns] = { ...existing, ...res }
    log.info(`Added ${Object.keys(res).length} translations to [${locale}] ${ns}`)
  }

  getResources(locale: SupportedLocale, ns: TranslationNamespace): Record<string, string> | undefined {
    return this.resources[locale]?.[ns] as Record<string, string> | undefined
  }

  getMissingKeys(): Array<{ key: string; ns: string; locale: string }> {
    return [...this.missingKeys]
  }

  /* ── Config ── */

  getConfig(): I18nConfig {
    return { ...this.config }
  }

  updateConfig(partial: Partial<I18nConfig>): void {
    this.config = { ...this.config, ...partial }
    if (partial.locale) {this.setLocale(partial.locale)}
    log.info('I18n config updated', partial)
  }

  /* ── Events ── */

  onLocaleChange(callback: (locale: SupportedLocale) => void): () => void {
    this.listeners.push(callback)
    return () => { this.listeners = this.listeners.filter(l => l !== callback) }
  }

  /* ── Validation ── */

  validateTranslations(): TranslationHealthReport {
    const baseLocale = this.config.fallbackLocale
    const baseNs = this.resources[baseLocale] || {}
    const results: TranslationValidationResult[] = []
    const allLocales = Object.keys(LOCALE_INFO) as SupportedLocale[]
    const namespaces = Object.keys(baseNs) as TranslationNamespace[]

    for (const locale of allLocales) {
      for (const ns of namespaces) {
        const baseKeys = Object.keys((baseNs[ns] || {}) as Record<string, string>)
        const localeNs = (this.resources[locale]?.[ns] || {}) as Record<string, string>
        const localeKeys = Object.keys(localeNs)

        const missingKeys = baseKeys.filter(k => !(k in localeNs))
        const extraKeys = localeKeys.filter(k => !baseKeys.includes(k))
        const emptyValues = localeKeys.filter(k => localeNs[k] === '')
        const totalKeys = baseKeys.length
        const coverage = totalKeys > 0 ? Math.round(((totalKeys - missingKeys.length) / totalKeys) * 100) : 0

        results.push({ locale, namespace: ns, missingKeys, extraKeys, emptyValues, totalKeys, coverage })
      }
    }

    const overallCoverage = results.length > 0
      ? Math.round(results.reduce((s, r) => s + r.coverage, 0) / results.length)
      : 0

    const recommendations: string[] = []
    const incompleteLocales = allLocales.filter(l => {
      const localeResults = results.filter(r => r.locale === l)
      return localeResults.some(r => r.coverage < 100)
    })
    for (const l of incompleteLocales) {
      const info = LOCALE_INFO[l]
      if (info.status === 'planned') {
        recommendations.push(`${info.nativeName} (${l}) 翻译待实现`)
      } else {
        const missing = results.filter(r => r.locale === l).reduce((s, r) => s + r.missingKeys.length, 0)
        if (missing > 0) {recommendations.push(`${info.nativeName} (${l}) 缺少 ${missing} 个翻译键`)}
      }
    }
    if (recommendations.length === 0) {recommendations.push('所有已启用语言翻译完整')}

    return { timestamp: Date.now(), locales: results, overallCoverage, recommendations }
  }
}

